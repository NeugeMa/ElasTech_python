# Operações Aritméticas 
''' 1. Soma: +
    2. Subtração: - 
    3. Multiplicação: * 
    4. Divisão: / 
    
    5. Divisão Inteira: //
    6. Resto da divisão: %
    7. Potência: **    '''

num1 = 10
num2 = 5

print(num1 + num2)
print(num1 - num2)
print(num1 * num2)
print(num1 / num2)

print(num1 // num2)
print(num1 % num2)
print(num1 ** num2)


# Booleanos 
idade1 = 20
idade2 = 21

print(idade1 > idade2)  # Verificando se idade1 é maior que idade2 = FALSE
print(idade1 <= idade2) # Verificando se idade1 é menor/igual que idade 2 = TRUE

print('Mari' == 'mari') # Fazendo uma comparação de texto, se eles são "iguais" = FALSE


